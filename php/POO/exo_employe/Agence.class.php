{

    /*****************Attributs***************** */
    private $_nom;
    private $_adresse;
    private $_postal;
    private $_ville;
    private $_resto;

    #region /*****************Accesseurs***************** */
    public function getNom()
    {
        return $this->_nom;
    }

    public function setNom($nom)
    {
        $this->_nom = $nom;
    }

    public function getAdresse()
    {
        return $this->_adresse;
    }

    public function setAdresse($adresse)
    {
        $this->_adresse = $adresse;
    }

    public function getPostal()
